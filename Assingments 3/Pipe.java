
public class Pipe {
	int pipeNumber;
	Object pipe;
	
	public Pipe(){
		
	}
	
	public Pipe(int pipeNumber, Object pipe) {
		this.pipeNumber = pipeNumber;
		this.pipe = pipe;
	}

	public int getPipeNumber() {
		return pipeNumber;
	}

	public void setPipeNumber(int pipeNumber) {
		this.pipeNumber = pipeNumber;
	}
	
	
	
}
